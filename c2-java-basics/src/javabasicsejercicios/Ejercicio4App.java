package javabasicsejercicios;

public class Ejercicio4App {

	public static void main(String[] args) {
		int N = 4;
		//Defino la variable entera N
		
		System.out.println("Valor de la variable N = "+N);
		//Le sumo 77 a N
		N += 77;
		System.out.println("Incrementa N en 77 = "+N);
		//Le resto 3 al anterior valor 
		N -= 3;
		System.out.println("Decrementa N en 3 = "+N);
		//Multiplico el resultado de la última operación por 2
		N *= 2;
		System.out.println("Duplica N = "+N);
		
	}

}
